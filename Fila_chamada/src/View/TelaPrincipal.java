    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed

LocalDateTime agora = LocalDateTime.now();

DateTimeFormatter formatterData = DateTimeFormatter.ofPattern("dd/MM/uuuu");
String dataFormatada = formatterData.format(agora);

DateTimeFormatter formatterHora = DateTimeFormatter.ofPattern("HH:mm:ss");
String horaFormatada = formatterHora.format(agora);

String HoraAtualizada = (horaFormatada + "  "+ dataFormatada);
     
        
        if (lbIdade.getText().isEmpty() || lbNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(
                    null, "Preencha todos os campos");
        } else {
            lbHora.setText(HoraAtualizada);
            
            pessoa.setNome(lbNome.getText());
            pessoa.setIdade(Integer.parseInt(lbIdade.getText()));
            pessoa.setHora(lbHora.getText());

            fila.inserir(pessoa.getNome(), pessoa.getIdade(),pessoa.getHora());
            fila.ordena();
            lbIdade.setText("");
            lbNome.setText("");
            lbHora.setText("");

            CopiaVet = fila.retornavetor();

            AntigaTabela(); 
            NovaTabela();
        }
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void btChamarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btChamarActionPerformed

        topo = fila.chamar();
        if (topo == null) {
            txtPainel.setText("");
            JOptionPane.showMessageDialog(
                    null, "Cadastre um Cliente !");
        } else {
            txtPainel.setText(topo.getNome());
            tablemodel.removeRow(topo);
        }
    }//GEN-LAST:event_btChamarActionPerformed

