    public void AntigaTabela(){
        for (int g = 0; g <= CopiaVet.length; g++) {
                if (CopiaVet[g] != null) {
                    tablemodel.removeRow(CopiaVet[g]);
                } else {
                    break;
                }
            }
    }
    public void NovaTabela(){
         for (int i = 0; i <= CopiaVet.length; i++) {
                if (CopiaVet[i] != null) {
                    tablemodel.addRow(CopiaVet[i]);
                } else {
                    break;
                }
            }
    }
    
    
    
